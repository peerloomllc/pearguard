// electron-builder afterPack hook: make the AppImage survive Ubuntu's sandbox.
//
// An AppImage ships chrome-sandbox inside a read-only mount, so it can NEVER be
// made SUID (mode 4755). On Ubuntu 23.10+/24.04, AppArmor
// (kernel.apparmor_restrict_unprivileged_userns=1) blocks the unprivileged
// user-namespace sandbox at runtime, so Chromium falls back to the SUID sandbox
// and aborts:
//   FATAL:setuid_sandbox_host.cc(163)] The SUID sandbox helper binary was found,
//   but is not configured correctly ... mode 4755.
// The .deb fixes this with a 4755 postinst (build/deb-postinst.sh); the AppImage
// cannot. Setting ELECTRON_DISABLE_SANDBOX from JS is TOO LATE - Chromium aborts
// during C++ init before the main script runs (verified on the test VM). The
// only reliable fix is to pass --no-sandbox in the real launch env, on every
// launch path (direct ./App.AppImage, --appimage-extract-and-run, and the
// desktop entry).
//
// afterPack runs once on the shared linux-unpacked dir before BOTH the deb and
// AppImage are built from it, so the wrapper ends up in both. To avoid weakening
// the .deb (which has a working SUID sandbox) we decide at RUNTIME: the AppImage
// runtime exports APPIMAGE/APPDIR, the .deb sets neither, so --no-sandbox is
// added only under an AppImage.
const fs = require('fs')
const path = require('path')

exports.default = async function afterPack(context) {
  if (context.electronPlatformName !== 'linux') return

  const exe = (context.packager && context.packager.executableName) || 'pearguard'
  const dir = context.appOutDir
  const real = path.join(dir, exe)
  const renamed = path.join(dir, exe + '.bin')

  if (!fs.existsSync(real)) {
    throw new Error(`afterPack: expected electron binary at ${real}`)
  }
  // Guard against double-wrapping if a packer ever calls afterPack twice.
  if (fs.existsSync(renamed)) return

  fs.renameSync(real, renamed)

  const wrapper = `#!/bin/bash
# Auto-generated by build/afterPack.js. Adds --no-sandbox only when launched
# from an AppImage (read-only mount cannot host a SUID chrome-sandbox). The .deb
# leaves APPIMAGE/APPDIR unset and keeps its proper SUID sandbox.
HERE="$(dirname "$(readlink -f "$0")")"
if [ -n "$APPIMAGE" ] || [ -n "$APPDIR" ]; then
  exec "$HERE/${exe}.bin" --no-sandbox "$@"
fi
exec "$HERE/${exe}.bin" "$@"
`
  fs.writeFileSync(real, wrapper, { mode: 0o755 })
  console.log(`  • afterPack: wrapped ${exe} with AppImage-only --no-sandbox launcher`)
}
